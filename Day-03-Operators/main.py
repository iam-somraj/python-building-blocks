#Arithmetic Operators
a = 6
b = 2
c = a+b
c1 = a-b
c2 = a*b
c3 = a/b

print(f"a = {a}, b = {b}, c = {c}, c1 = {c1}, c2 = {c2}, c3 = {c3}");

#Output:
8, 4, 12, 3.0

#-----------------------------------------------------------------------------------

# Modulo Operator
a = 9
b = a%2

print(f"a = {a}")
print(f"b = {b}")

#Output:
a = 9
b = 1

#------------------------------------------------------------------------------------

#Arithmetic Shortcuts
c = 4
c *= 2
c -= 1 

print(f"c = {c}")

#Output:
c = 7

#------------------------------------------------------------------------------------

